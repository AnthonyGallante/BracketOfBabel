import { readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

function parseCsvLine(line) {
  const out = [];
  let cur = "";
  let inQuotes = false;
  for (let i = 0; i < line.length; i++) {
    const ch = line[i];
    if (ch === "\"") {
      if (inQuotes && line[i + 1] === "\"") {
        cur += "\"";
        i++;
      } else {
        inQuotes = !inQuotes;
      }
    } else if (ch === "," && !inQuotes) {
      out.push(cur);
      cur = "";
    } else {
      cur += ch;
    }
  }
  out.push(cur);
  return out;
}

async function main() {
  const here = dirname(fileURLToPath(import.meta.url));
  const frontendRoot = resolve(here, "..");
  const repoRoot = resolve(frontendRoot, "..");
  const csvPath = resolve(repoRoot, "data", "ELO_Ratings.csv");
  const teamsPath = resolve(frontendRoot, "src", "engine", "teams.js");
  const outPath = resolve(frontendRoot, "src", "engine", "eloRatings.generated.js");

  const csvText = await readFile(csvPath, "utf8");
  const lines = csvText.split(/\r?\n/).filter(Boolean);
  const header = parseCsvLine(lines[0]);
  const schoolIdx = header.indexOf("School");
  const eloIdx = header.indexOf("ELO");
  if (schoolIdx === -1 || eloIdx === -1) {
    throw new Error("ELO_Ratings.csv missing required School/ELO columns.");
  }

  const eloByName = new Map();
  for (let i = 1; i < lines.length; i++) {
    const row = parseCsvLine(lines[i]);
    const school = (row[schoolIdx] ?? "").trim();
    const eloRaw = (row[eloIdx] ?? "").trim();
    if (!school || !eloRaw) continue;
    const elo = Number(eloRaw);
    if (!Number.isFinite(elo)) continue;
    eloByName.set(school, elo);
  }

  const teamsText = await readFile(teamsPath, "utf8");
  const teamRegex = /\[\s*\d+\s*,\s*"[^"]+"\s*,\s*"([^"]+)"\s*,\s*"([^"]+)"\s*\]/g;
  const teams = [];
  let m;
  while ((m = teamRegex.exec(teamsText)) !== null) {
    teams.push({ name: m[1], slug: m[2] });
  }

  const missing = teams.filter((t) => !eloByName.has(t.name)).map((t) => t.name);
  if (missing.length) {
    throw new Error(`Missing Elo values for team names: ${missing.join(", ")}`);
  }

  const entries = teams
    .map((t) => `  "${t.slug}": ${eloByName.get(t.name)},`)
    .join("\n");

  const output = `// AUTO-GENERATED by scripts/generate-elo-ratings.mjs\n// Do not edit manually.\n\nexport const ELO_BY_SLUG = {\n${entries}\n};\n`;

  await writeFile(outPath, output, "utf8");
  console.log(`Generated ${outPath}`);
}

main().catch((err) => {
  console.error(err);
  process.exitCode = 1;
});

